@Composable
fun PTTextField(
    value: String,
    onValueChange: (String) -> Unit,
    modifier: Modifier = Modifier,
    enabled: Boolean = true,
    readOnly: Boolean = false,
    textStyle: TextStyle = LocalTextStyle.current,
    label: @Composable (() -> Unit)? = null,
    placeholder: @Composable (() -> Unit)? = null,
    leadingIcon: @Composable (() -> Unit)? = null,
    trailingIcon: @Composable (() -> Unit)? = null,
    isError: Boolean = false,
    visualTransformation: VisualTransformation = VisualTransformation.None,
    keyboardOptions: KeyboardOptions = KeyboardOptions.Default,
    keyboardActions: KeyboardActions = KeyboardActions(),
    singleLine: Boolean = false,
    maxLines: Int = Int.MAX_VALUE,
    interactionSource: MutableInteractionSource = remember { MutableInteractionSource() },
    shape: Shape = MaterialTheme.shapes.small.copy(bottomEnd = ZeroCornerSize, bottomStart = ZeroCornerSize),
    colors: TextFieldColors = TextFieldDefaults.textFieldColors()
) {
    TextField(
        value = value,
        onValueChange = onValueChange,
        modifier = modifier.handleKeyboardEventsFocus(isSingleLine = singleLine || maxLines == 1),
        enabled = enabled,
        readOnly = readOnly,
        textStyle = textStyle,
        label = label,
        placeholder = placeholder,
        leadingIcon = leadingIcon,
        trailingIcon = trailingIcon,
        isError = isError,
        visualTransformation = visualTransformation,
        keyboardOptions = keyboardOptions,
        keyboardActions = keyboardActions,
        singleLine = singleLine,
        maxLines = maxLines,
        interactionSource = interactionSource,
        shape = shape,
        colors = colors
    )
}

@OptIn(ExperimentalComposeUiApi::class)
private fun Modifier.handleKeyboardEventsFocus(isSingleLine: Boolean) = composed {
    val focusManager = LocalFocusManager.current
    then(
        onPreviewKeyEvent {
            with(FocusDirection) {
                if (it.type == KeyEventType.KeyDown) {
                    focusManager.moveFocus(
                        when (it.key) {
                            Key.Tab -> if (it.isShiftPressed) Previous else Next
                            Key.DirectionDown -> if (isSingleLine) Down else return@onPreviewKeyEvent false
                            Key.DirectionUp -> if (isSingleLine) Up else return@onPreviewKeyEvent false
                            else -> return@onPreviewKeyEvent false
                        }
                    )
                    return@onPreviewKeyEvent true
                }
            }
            false
        }
    )
}
